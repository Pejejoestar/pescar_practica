primer_num = int(input('Ingrese un número '))
segundo_num = int(input('Ingrese un número más '))

cociente = primer_num / segundo_num

resto = primer_num % segundo_num # Revisar este valor de acá, no corresponde a, por ej, 39 / 5

print(f"División cociente: {int(cociente)}")
print(f"División resto: {resto}")